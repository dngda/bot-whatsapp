/**
 * Get Time duration
 * @param  {Date} timestamp
 * @param  {Date} now
 */
const processTime = (timestamp, now) => {
    // timestamp => timestamp when message was received
    return duration(now - moment(timestamp * 1000)).asSeconds()
}

/**
 * is it url?
 * @param  {String} url
 */
const isUrl = (url) => {
    return url.match(new RegExp(/https?:\/\/(www\.)?[-a-zA-Z0-9@:%._+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b([-a-zA-Z0-9()@:%_+.~#?&/=]*)/gi))
}

// Message Filter / Message Cooldowns
const usedCommandRecently = new Set()

/**
 * Check is number filtered
 * @param  {String} from
 */
const isFiltered = (from) => {
    return !!usedCommandRecently.has(from)
}

/**
 * Add number to filter
 * @param  {String} from
 */
const addFilter = (from, delay) => {
    usedCommandRecently.add(from)
    setTimeout(() => {
        return usedCommandRecently.delete(from)
    }, delay) // 1sec is delay before processing next command
}

/**
 *Download any media from URL
 *@param {String} url
 *@param {Path} locate
 *@param {Callback} callback
 */
const download = (url, path, callback) => {
    head(url, () => {
        request(url)
            .pipe(createWriteStream(path))
            .on('close', callback)
    })
}

/**
 *@param {String} url
 */
const redir = (url) => {
    get(url, response => {
        return response.responseUrl
    })
}

const createReadFileSync = (path) => {
    if (existsSync(path)) {
        return readFileSync(path)
    }
    else {
        writeFileSync(path, '[]')
        return readFileSync(path)
    }
}

function formatin(duit) {
    let reverse = duit.toString().split('').reverse().join('')
    let ribuan = reverse.match(/\d{1,3}/g)
    ribuan = ribuan.join('.').split('').reverse().join('')
    return ribuan
}

const inArray = (needle, haystack) => {
    let length = haystack.length
    for (let i = 0; i < length; i++) {
        if (haystack[i].id == needle) return i
    }
    return -1
}

const last = (array, n) => {
    if (array == null) return void 0
    if (n == null) return array[array.length - 1]
    return array.slice(Math.max(array.length - n, 0))
}

const unlinkIfExists = (path, path2) => {
    if (existsSync(path)) unlinkSync(path)
    if (existsSync(path2)) unlinkSync(path2)
}

String.prototype.toDHms = function () {
    var sec_num = parseInt(this, 10) // don't forget the second param
    var hours = Math.floor(sec_num / 3600)
    var minutes = Math.floor((sec_num - (hours * 3600)) / 60)
    var seconds = sec_num - (hours * 3600) - (minutes * 60)
    var days = 0
    if (hours >= 24) { days = Math.floor(hours / 24); hours = hours % 24 }
    var time = days + ' days ' + hours + ' hours ' + minutes + ' minutes ' + seconds + ' secs'
    return time
}


